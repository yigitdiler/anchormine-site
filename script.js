window.addEventListener("load", () => {
  const requestedSection = new URLSearchParams(window.location.search).get("view");
  const targetSelector = window.location.hash || (requestedSection ? `#${requestedSection}` : "");
  if (targetSelector) {
    window.setTimeout(() => {
      document.querySelector(targetSelector)?.scrollIntoView({ block: "start" });
      window.scrollBy(0, -76);
    }, 300);
  }
});
